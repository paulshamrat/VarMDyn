def paste_centered(canvas, image, box: tuple[int, int, int, int]) -> None:
    x0, y0, x1, y1 = box
    box_w = x1 - x0
    box_h = y1 - y0
    scale = min(box_w / image.width, box_h / image.height)
    new_size = (max(1, round(image.width * scale)), max(1, round(image.height * scale)))
    resampling = getattr(image, "Resampling", None)
    resized = image.resize(new_size, resampling.LANCZOS if resampling else 1)
    canvas.paste(resized, (x0 + (box_w - resized.width) // 2, y0 + (box_h - resized.height) // 2))


