def read_bottleneck_residues(path: Path, top_n: int) -> list[int]:
    residues: list[int] = []
    for row in csv_rows(path):
        if len(residues) >= top_n:
            break
        selection = row.get("Selection", "")
        match = SEL_RESID_RE.search(selection)
        if match:
            residues.append(int(match.group(1)))
            continue
        value = row.get("residue_number", "") or row.get("ResidueNumber", "")
        if value:
            residues.append(int(float(value)))
    seen: set[int] = set()
    ordered: list[int] = []
    for residue in residues:
        if residue not in seen:
            seen.add(residue)
            ordered.append(residue)
    return ordered


