def plot_overlap_state(state: str, out_dir: Path) -> list[Path]:
    configure_matplotlib_cache()
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise SystemExit(f"missing matplotlib for network figures: {exc}") from exc

    table = require_file(DATA_ROOT / "compare" / state / "overlap_with_WT.csv", f"{state} overlap table")
    rows = csv_rows(table)
    if not rows:
        raise SystemExit(f"empty {state} overlap table: {table}")
    variants = [row["variant"] for row in rows]
    shared = [int(float(row["shared"])) for row in rows]
    wt_lost = [int(float(row["wt_lost"])) for row in rows]
    gained = [int(float(row["gained"])) for row in rows]

    fig, ax = plt.subplots(figsize=(max(6.0, len(variants) * 0.9), 4.2))
    x = list(range(len(variants)))
    ax.bar(x, shared, label="Shared with WT", color="#4C956C")
    ax.bar(x, wt_lost, bottom=shared, label="WT lost", color="#2C5AA0")
    ax.bar(
        x,
        gained,
        bottom=[a + b for a, b in zip(shared, wt_lost)],
        label="Variant gained",
        color="#D97904",
    )
    ax.set_title(f"{state.upper()} WT top-25 bottleneck overlap")
    ax.set_ylabel("Residue count")
    ax.set_xticks(x)
    ax.set_xticklabels(variants, rotation=35, ha="right")
    ax.set_ylim(0, max([a + b + c for a, b, c in zip(shared, wt_lost, gained)] + [25]) + 2)
    ax.legend(frameon=False, ncols=3, loc="upper center", bbox_to_anchor=(0.5, 1.18))
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    fig.tight_layout()
    out_base = out_dir / f"{state}_wt_overlap"
    save_figure(fig, out_base)
    plt.close(fig)
    return [out_base.with_suffix(".png"), out_base.with_suffix(".svg")]


def plot_transition_frequency_state(state: str, out_dir: Path, top_n: int) -> list[Path]:
    configure_matplotlib_cache()
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise SystemExit(f"missing matplotlib for network figures: {exc}") from exc

    table = require_file(DATA_ROOT / "compare" / state / "transition_frequency.csv", f"{state} transition-frequency table")
    rows = csv_rows(table)
    if not rows:
        raise SystemExit(f"empty {state} transition-frequency table: {table}")

    lost = [row for row in rows if row["transition_class"] == "wt_lost"][:top_n]
    gained = [row for row in rows if row["transition_class"] == "gained"][:top_n]
    labels = [row["residue"] for row in lost] + [row["residue"] for row in gained]
    values = [-int(row["count"]) for row in lost] + [int(row["count"]) for row in gained]
    colors = ["#2C5AA0"] * len(lost) + ["#D97904"] * len(gained)
