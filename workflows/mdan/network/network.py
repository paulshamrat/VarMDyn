def parse_pdb_atoms(path: Path) -> tuple[
    list[str],
    dict[tuple[str, str, str, str], np.ndarray],
    dict[tuple[str, str, str], np.ndarray],
]:
    lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    ca_coords: dict[tuple[str, str, str, str], np.ndarray] = {}
    residue_ca_coords: dict[tuple[str, str, str], np.ndarray] = {}
    for line in lines:
        if not line.startswith(("ATOM  ", "HETATM")):
            continue
        atom_name = line[12:16].strip()
        if atom_name != "CA":
            continue
        chain = line[21].strip()
        resseq = line[22:26].strip()
        icode = line[26].strip()
        resname = line[17:20].strip()
        key = (chain, resseq, icode, resname)
        ca_coords[key] = np.array([float(line[30:38]), float(line[38:46]), float(line[46:54])], dtype=float)
        residue_ca_coords[(resseq, icode, resname)] = ca_coords[key]
    return lines, ca_coords, residue_ca_coords


def kabsch_transform(mobile: np.ndarray, reference: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    mobile_center = mobile.mean(axis=0)
    reference_center = reference.mean(axis=0)
    mobile_centered = mobile - mobile_center
    reference_centered = reference - reference_center
    covariance = mobile_centered.T @ reference_centered
    v, _s, wt = np.linalg.svd(covariance)
    correction = np.eye(3)
    correction[2, 2] = np.linalg.det(v @ wt)
    rotation = v @ correction @ wt
    translation = reference_center - mobile_center @ rotation
    return rotation, translation


def aligned_pdb_for_surface_render(pdb: Path, reference: Path | None, out_pdb: Path) -> Path:
    if reference is None:
        return pdb
    reference = require_file(reference, "residue-remodel reference PDB")
    lines, mobile_ca, mobile_residue_ca = parse_pdb_atoms(pdb)
    _reference_lines, reference_ca, reference_residue_ca = parse_pdb_atoms(reference)
    common = sorted(set(mobile_ca) & set(reference_ca), key=residue_sort_key)
    if len(common) < 20:
        residue_common = sorted(set(mobile_residue_ca) & set(reference_residue_ca), key=lambda key: residue_sort_key(("", *key)))
        mobile = np.array([mobile_residue_ca[key] for key in residue_common], dtype=float)
        reference_xyz = np.array([reference_residue_ca[key] for key in residue_common], dtype=float)
        match_count = len(residue_common)
    else:
        mobile = np.array([mobile_ca[key] for key in common], dtype=float)
        reference_xyz = np.array([reference_ca[key] for key in common], dtype=float)
        match_count = len(common)
    if match_count < 20:
        raise SystemExit(f"not enough matching CA atoms to align {pdb} to {reference}: {match_count}")
    rotation, translation = kabsch_transform(mobile, reference_xyz)

    aligned: list[str] = []
    for line in lines:
        if line.startswith(("ATOM  ", "HETATM")) and len(line) >= 54:
            xyz = np.array([float(line[30:38]), float(line[38:46]), float(line[46:54])], dtype=float)
            transformed = xyz @ rotation + translation
            line = f"{line[:30]}{transformed[0]:8.3f}{transformed[1]:8.3f}{transformed[2]:8.3f}{line[54:]}"
        aligned.append(line)
    out_pdb.parent.mkdir(parents=True, exist_ok=True)
    out_pdb.write_text("\n".join(aligned) + "\n", encoding="utf-8")
    print(f"[OK] aligned surface render input: {out_pdb} ({match_count} CA atoms)")
    return out_pdb


