def crop_white_image(img, pad: int = 4):
    from PIL import ImageOps

    arr = np.array(img.convert("RGB"))
    mask = np.any(arr < 242, axis=2)
    h, _w = mask.shape
    mask[int(h * 0.90) :, :] = False
    if not mask.any():
        return img
    ys, xs = np.where(mask)
    y0, y1 = int(ys.min()), int(ys.max()) + 1
    x0, x1 = int(xs.min()), int(xs.max()) + 1
    cropped = img.crop((max(0, x0 - pad), max(0, y0 - pad), min(arr.shape[1], x1 + pad), min(arr.shape[0], y1 + pad)))
    return ImageOps.expand(apply_dynamics_protein_style(cropped.convert("RGB")), border=18, fill="white")


def apply_dynamics_protein_style(img):
    """Fade protein while preserving network marks."""
    from PIL import Image

    arr = np.array(img.convert("RGB"), dtype=np.float32)
    channel_range = arr.max(axis=2) - arr.min(axis=2)
    nonwhite = np.any(arr < 246, axis=2)
    protein_mask = nonwhite & (channel_range < 42)
    styled = arr.copy()
    protein = styled[protein_mask]
    if protein.size:
        wheat_tinted = 0.25 * protein + 0.75 * PROTEIN_WHEAT_RGB
        styled[protein_mask] = 0.58 * wheat_tinted + 0.42 * WHITE_RGB
    return Image.fromarray(np.clip(styled, 0, 255).astype(np.uint8), mode="RGB")


