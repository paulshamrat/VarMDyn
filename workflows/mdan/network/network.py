def check_prepared_frames(args: argparse.Namespace) -> None:
    try:
        import MDAnalysis as mda
    except ImportError as exc:
        raise SystemExit(f"missing MDAnalysis for frame check: {exc}") from exc

    variants = array_variants(args)
    prepared_root = network_data_root() / "prepared" / args.state
    required_frames = args.num_sampled_frames + 1
    failures = 0
    for variant in variants:
        dcd = prepared_root / variant / f"{variant}.dcd"
        if not dcd.exists():
            print(f"FAIL {args.state}/{variant}: missing prepared DCD {dcd}")
            failures += 1
            continue
        frames = len(mda.Universe(str(dcd)).trajectory)
        status = "OK" if frames >= required_frames else "FAIL"
        print(
            f"{status} {args.state}/{variant}: frames={frames}, "
            f"required>={required_frames}, dcd={dcd}"
        )
        if status != "OK":
            failures += 1
    if failures:
        raise SystemExit(f"{failures} variants are not ready for {args.num_sampled_frames} sampled frames")


