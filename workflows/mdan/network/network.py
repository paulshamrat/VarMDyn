def content_bbox_image(img, white_cutoff: int = 248) -> tuple[int, int, int, int]:
    rgb = img.convert("RGB")
    arr = np.array(rgb)
    mask = np.any(arr < white_cutoff, axis=2)
    if not mask.any():
        return (0, 0, img.width, img.height)
    ys, xs = np.where(mask)
    return (int(xs.min()), int(ys.min()), int(xs.max()) + 1, int(ys.max()) + 1)


def crop_pngs(paths: list[Path], padding_x: int, padding_top: int, padding_bottom: int) -> list[Path]:
    from PIL import Image

    outputs: list[Path] = []
    for path in paths:
        img = Image.open(path)
        left, top, right, bottom = content_bbox_image(img)
        left = max(0, left - padding_x)
        top = max(0, top - padding_top)
        right = min(img.width, right + padding_x)
        bottom = min(img.height, bottom + padding_bottom)
        out = path.with_name(path.stem + "_cropped.png")
        img.crop((left, top, right, bottom)).save(out)
        outputs.append(out)
    return outputs


def png_size(path: Path) -> tuple[int, int]:
    with path.open("rb") as handle:
        handle.read(8)
        length = struct.unpack(">I", handle.read(4))[0]
        handle.read(4)
        data = handle.read(length)
    return struct.unpack(">II", data[:8])


def embed_png(path: Path) -> str:
    return base64.b64encode(path.read_bytes()).decode("ascii")


